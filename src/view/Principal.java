    private void bt_negativoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_negativoActionPerformed
        // TODO add your handling code here:
        bffImgModificada = new BufferedImage(bffImg.getWidth(), bffImg.getHeight(), BufferedImage.TYPE_INT_RGB);

        int valorR, valorG, valorB;

        for (int i = 0; i < bffImg.getHeight() - 1; i++) {
            for (int j = 0; j < bffImg.getWidth() - 1; j++) {
                Color cor = new Color(bffImg.getRGB(i, j));

                valorR = 255 - cor.getRed();
                valorG = 255 - cor.getGreen();
                valorB = 255 - cor.getBlue();

                cor = new Color(valorR, valorG, valorB);
                bffImgModificada.setRGB(i, j, cor.getRGB());
            }
        }

        ImageIcon img_icon = new ImageIcon(bffImgModificada);
        img_icon.setImage(img_icon.getImage().getScaledInstance(lb_modificado.getWidth(), lb_modificado.getHeight(), 0));
        lb_modificado.setIcon(img_icon);
    }//GEN-LAST:event_bt_negativoActionPerformed

    private void bt_logActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_logActionPerformed
        bffImgModificada = new BufferedImage(bffImg.getWidth(), bffImg.getHeight(), BufferedImage.TYPE_INT_RGB);
        int valorR, valorG, valorB;

        for (int i = 0; i < bffImg.getHeight() - 1; i++) {
            for (int j = 0; j < bffImg.getWidth() - 1; j++) {
                Color cor = new Color(bffImg.getRGB(i, j));

                valorR = (int) (105.886 * Math.log10(1 + cor.getRed()));
                valorG = (int) (105.886 * Math.log10(1 + cor.getGreen()));
                valorB = (int) (105.886 * Math.log10(1 + cor.getBlue()));

                cor = new Color(valorR, valorG, valorB);
                bffImgModificada.setRGB(i, j, cor.getRGB());
            }
        }

        ImageIcon img_icon = new ImageIcon(bffImgModificada);
        img_icon.setImage(img_icon.getImage().getScaledInstance(lb_modificado.getWidth(), lb_modificado.getHeight(), 0));
        lb_modificado.setIcon(img_icon);
    }//GEN-LAST:event_bt_logActionPerformed

