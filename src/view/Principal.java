    private void bt_abrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_abrirActionPerformed
        JFileChooser arquivo = new JFileChooser();
        arquivo.setFileSelectionMode(JFileChooser.FILES_ONLY);

        int opc = arquivo.showOpenDialog(this);
        if (opc == JFileChooser.APPROVE_OPTION) {
            File file = arquivo.getSelectedFile();
            file = arquivo.getSelectedFile();

            try {
                bffImg = ImageIO.read(file);
                ImageIcon img_icon = new ImageIcon(bffImg);
                img_icon.setImage(img_icon.getImage().getScaledInstance(lb_original.getWidth(), lb_original.getHeight(), 0));
                lb_original.setIcon(img_icon);

            } catch (IOException ex) {
                Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_bt_abrirActionPerformed

